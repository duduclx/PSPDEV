/*

 NOVAETS - KIS2 (might also works for other Novaets keyboards)

 This keyboard is some really braindead peace of hardware.

 Key press/release will give a 2byte result. This is the simple part.

 First braindead part:

 A second press of the key will give a differend 2byte result.
 I figured out, that key press/release is some cycle. That means every press + release
 will lead to a cycle. Doing that  for 32 time the cycle starts again. With a small programm 
 i dumped and create this map file.

 -> So we can guess row/col.

 Second braindead part:
 
 We can't guess a release! The release codes of 61 keys all appear in their cycles.

 The only release event that can be guessed corretly are the following:
 - Left Shift
 - Left Ctrl
 - Left Alt
 - Left Fn
 - Punct

 */

#include <stdio.h>
#include "pspirkeyb_rawkeys.h"
#include "novaetsmap.h"

/* cylce of one single key */
#define NOVAETS_CYCLE_MAX 32

/* typedef for a single dump of a keycycle */
typedef unsigned char novaets_cycle_keycode_entry[NOVAETS_CYCLE_MAX][2];

/* this is a table containing all keycode cycles*/
static novaets_cycle_keycode_entry * novaets_cycle_keycode_table[NOVAETS_KIS2_KEY_ROWS][NOVAETS_KIS2_KEY_COLS];

/* modkey positions */
struct NovaEtsCycleModKey
{
    int row;
    int col;
    unsigned char key;
};

/* table with modrows - last entry has negative row/cols */
static struct NovaEtsCycleModKey novaets_cycle_modkey_table[];

/* checks if the row/col is a modified key */
static int novaets_is_mod( int row, int col );

/* */
int novaets_kis2_cycle_decode( unsigned char data[2], int *row, int *col, unsigned char *pressed, int *ismod )
{
	int colCur, rowCur;
	int i, keyFound = 0;

    if( !data || !row || !col || !pressed || !ismod )
        return -1;
    
    *ismod = 0;
    *row = -1;
    *col = -1;

    /* press or release */
    *pressed = 0;
    if( data[0] > 127 )
        *pressed = 1;

	for( rowCur=0; rowCur < NOVAETS_KIS2_KEY_ROWS; rowCur ++ )
	{
		for( colCur=0; colCur < NOVAETS_KIS2_KEY_COLS; colCur++ )
		{
			novaets_cycle_keycode_entry * current 
                = novaets_cycle_keycode_table[rowCur][colCur];
			if( current == NULL )
				continue;

			for( i=0; i < NOVAETS_CYCLE_MAX; i++ )
			{

				if( (*current)[i][0] == data[0] &&
					(*current)[i][1] == data[1] )
				{
                    keyFound++;
                    if( (*pressed)==0 && keyFound > 1 )
                    {
                        /* hwlimit: can't guess this release */
                        return -1;
                    }
                    *col = colCur;
                    *row = rowCur;
                    /* we guessed the row for a key press*/
                    if( *pressed )
                    {
                        *ismod = novaets_is_mod( *row, *col );
                        return 0;
                    }
				}
			}
		}
	}

	if( keyFound != 1 )
	{
        /* error: unknown key */
		return -1;
	}

	if( *pressed )
	{
		/* error: this is no release */
		return -1;
	}

    /* we can only guess some special keys for release events */
    *ismod = novaets_is_mod( *row, *col );
    if( *ismod == 0 ) /* we expect a positive result here */
        return -1;
    return 0;
}

static int novaets_is_mod( int row, int col )
{
    int i;

    for( i=0; ; i++ )
    {
        struct NovaEtsCycleModKey * modKey;
        modKey = novaets_cycle_modkey_table + i;

        if( modKey->col < 0 || modKey->row < 0 )
        {
            /* key not found in table */
            return 0;
        }

        if( modKey->row == row && modKey->col == col )
        {
            return 1;
        }
    }

    return 0;
}


